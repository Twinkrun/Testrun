#import "UIColor+Twinkrun.h"

@implementation UIColor (Twinkrun)

+ (UIColor *)twinkrunBlack
{
    return [[UIColor alloc] initWithRed:0.2 green:0.2039 blue:0.2196 alpha:1.0];
}

+ (UIColor *)twinkrunGreen
{
    return [[UIColor alloc] initWithRed:0.0 green:0.8117 blue:0.6862 alpha:1.0];
}

+ (UIColor *)twinkrunRed
{
    return [[UIColor alloc] initWithRed:1.0 green:0.2549 blue:0.2 alpha:1.0];
}

+ (UIColor *)twinkrunWhite
{
    return [[UIColor alloc] initWithRed:1.0 green:1.0 blue:1.0 alpha:1.0];
}

@end
